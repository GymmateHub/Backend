package com.gymmate.health.domain.Enums;

/**
 * Enum representing workout intensity levels.
 * Used for tracking and analyzing workout difficulty.
 */
public enum WorkoutIntensity {
    LOW,          // Light activity, easy pace
    MEDIUM,       // Moderate effort, comfortable
    HIGH,         // Hard effort, challenging
    VERY_HIGH     // Maximum effort, near exhaustion
}
