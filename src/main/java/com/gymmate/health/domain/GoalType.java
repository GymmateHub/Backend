package com.gymmate.health.domain;

/**
 * Enum representing types of fitness goals members can set.
 */
public enum GoalType {
    WEIGHT_LOSS,              // Reduce body weight
    MUSCLE_GAIN,              // Increase muscle mass
    STRENGTH,                 // Improve strength metrics
    ENDURANCE,                // Improve cardiovascular endurance
    FLEXIBILITY,              // Improve flexibility and mobility
    BODY_FAT_REDUCTION,       // Reduce body fat percentage
    GENERAL_FITNESS,          // Overall fitness improvement
    SPECIFIC_EXERCISE,        // Master a specific exercise
    CONSISTENCY,              // Workout consistency goals
    COMPETITION_PREP          // Prepare for competition/event
}
